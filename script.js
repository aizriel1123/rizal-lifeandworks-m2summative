        // ===== RENDER FUNCTIONS =====
        function renderSection(data, containerId, sectionClass) {
            const container = document.getElementById(containerId);
            
            data.forEach((entry, index) => {
                const article = document.createElement('article');
                article.className = `timeline-entry ${sectionClass}`;
                article.setAttribute('tabindex', '0');
                article.setAttribute('role', 'article');
                article.setAttribute('data-index', index);
                article.setAttribute('data-section', sectionClass);
                
                let imageHTML = '';
                if (entry.image) {
                    imageHTML = `<img src="${entry.image}" alt="${entry.title}" class="card-image" loading="lazy">`;
                }
                
                article.innerHTML = `
                    <span class="category-stamp">${entry.stamp}</span>
                    <div class="card">
                        <time class="card-date">${entry.date}</time>
                        <h3 class="card-title">${entry.title}</h3>
                        ${imageHTML}
                        <div class="card-content collapsed">
                            ${entry.content}
                        </div>
                        <div class="card-actions">
                            <button class="btn btn-expand" onclick="toggleExpand(this)">Read More</button>
                        </div>
                    </div>
                `;
                
                container.appendChild(article);
            });
        }

        // ===== EXPAND/COLLAPSE FUNCTIONALITY =====
        function toggleExpand(button) {
            const card = button.closest('.card');
            const content = card.querySelector('.card-content');
            
            if (content.classList.contains('collapsed')) {
                content.classList.remove('collapsed');
                button.textContent = 'Read Less';
            } else {
                content.classList.add('collapsed');
                button.textContent = 'Read More';
            }
        }

        // ===== SCROLL REVEAL ANIMATION =====
        function revealOnScroll() {
            const entries = document.querySelectorAll('.timeline-entry');
            const windowHeight = window.innerHeight;
            
            entries.forEach(entry => {
                const entryTop = entry.getBoundingClientRect().top;
                
                if (entryTop < windowHeight * 0.85) {
                    entry.classList.add('visible');
                }
            });
        }

        // ===== PROGRESS BAR =====
        function updateProgressBar() {
            const scrollTotal = document.documentElement.scrollHeight - window.innerHeight;
            const scrollProgress = (window.scrollY / scrollTotal) * 100;
            document.getElementById('progressBar').style.width = scrollProgress + '%';
            
            // Update the section label
            const currentSection = getCurrentSection();
            const progressLabel = document.getElementById('progressLabel');
            if (progressLabel.textContent !== currentSection) {
                progressLabel.textContent = currentSection;
            }
        }

        function getCurrentSection() {
            const sections = [
                { id: 'introductionTimeline', name: 'Introduction' },
                { id: 'biologicalTimeline', name: 'II. Biological Factors' },
                { id: 'environmentalTimeline', name: 'III. Environmental Factors' },
                { id: 'experiencesTimeline', name: 'IV. Life Experiences' },
                { id: 'characterTimeline', name: 'V. Character Analysis' },
                { id: 'heroismTimeline', name: 'VI. Pitfalls and Heroism' }
            ];
            
            const conclusionSection = document.querySelector('.conclusion');
            
            // Check if conclusion is in view
            if (conclusionSection) {
                const conclusionRect = conclusionSection.getBoundingClientRect();
                if (conclusionRect.top < window.innerHeight / 2 && conclusionRect.bottom > 0) {
                    return 'VII. Conclusion';
                }
            }
            
            // Check which timeline section is currently in view
            for (let section of sections) {
                const element = document.getElementById(section.id);
                if (element) {
                    const rect = element.getBoundingClientRect();
                    // Section is in view if its top is above the middle of the screen
                    // and its bottom is below the top of the screen
                    if (rect.top < window.innerHeight / 2 && rect.bottom > 0) {
                        return section.name;
                    }
                }
            }
            
            return 'Introduction'; // Default to Introduction
        }

        // ===== KEYBOARD NAVIGATION =====
        let currentFocusIndex = -1;
        let allEntries = [];

        function handleKeyboardNavigation(e) {
            if (allEntries.length === 0) {
                allEntries = Array.from(document.querySelectorAll('.timeline-entry'));
            }
            
            if (e.key === 'ArrowDown') {
                e.preventDefault();
                currentFocusIndex = Math.min(currentFocusIndex + 1, allEntries.length - 1);
                allEntries[currentFocusIndex].focus();
                allEntries[currentFocusIndex].scrollIntoView({ behavior: 'smooth', block: 'center' });
            } else if (e.key === 'ArrowUp') {
                e.preventDefault();
                currentFocusIndex = Math.max(currentFocusIndex - 1, 0);
                allEntries[currentFocusIndex].focus();
                allEntries[currentFocusIndex].scrollIntoView({ behavior: 'smooth', block: 'center' });
            } else if (e.key === 'Enter' && currentFocusIndex >= 0) {
                e.preventDefault();
                const button = allEntries[currentFocusIndex].querySelector('.btn-expand');
                if (button) toggleExpand(button);
            }
        }

        // ===== EVENT LISTENERS =====
        window.addEventListener('scroll', () => {
            revealOnScroll();
            updateProgressBar();
        });

        document.addEventListener('keydown', handleKeyboardNavigation);

        // ===== INITIALIZE =====
        document.addEventListener('DOMContentLoaded', () => {
            // Render all sections
            renderSection(introductionData, 'introductionTimeline', 'introduction');
            renderSection(biologicalData, 'biologicalTimeline', 'biological');
            renderSection(environmentalData, 'environmentalTimeline', 'environmental');
            renderSection(experiencesData, 'experiencesTimeline', 'experiences');
            renderSection(characterData, 'characterTimeline', 'character');
            renderSection(heroismData, 'heroismTimeline', 'heroism');
            
            // Update entries array for keyboard navigation
            allEntries = Array.from(document.querySelectorAll('.timeline-entry'));
            
            // Initial reveal
            revealOnScroll();
            updateProgressBar();
            
            // Check for hash in URL
            const hash = window.location.hash;
            if (hash) {
                const hashParts = hash.substring(1).split('-');
                if (hashParts.length === 2) {
                    const section = hashParts[0];
                    const index = parseInt(hashParts[1]);
                    const entry = document.querySelector(`.timeline-entry[data-section="${section}"][data-index="${index}"]`);
                    if (entry) {
                        setTimeout(() => {
                            entry.scrollIntoView({ behavior: 'smooth', block: 'center' });
                        }, 500);
                    }
                }
            }
        });